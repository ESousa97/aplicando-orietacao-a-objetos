package Aula01.Desafio_pessoais.praticando5;

public class MaiorNumero {
    
    int numero1 = 4;
    int numero2 = 6;

    public void verificarMaior() {
        if (numero1 > numero2) {
            System.out.println(numero1 + " é maior que " + numero2);
        } else {
            System.out.println(numero2 + " é maior que " + numero1);
        }
    }
}


